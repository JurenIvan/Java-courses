package hr.fer.zemris.java.hw03.prob1;

/**
 * Enum used to describe possible types of tokens.
 * 
 * @author juren
 *
 */
public enum TokenType {
	/**
	 * Represents End Of File
	 */
	EOF,

	/**
	 * Represents Word-Token
	 */
	WORD,

	/**
	 * Represents Number-Token
	 */
	NUMBER,

	/**
	 * Represents Symbol-Token
	 */
	SYMBOL;
}
